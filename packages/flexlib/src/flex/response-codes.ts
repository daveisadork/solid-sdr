export function describeResponseCode(code: number): string | undefined {
  return responseCodeDescriptions.get(code);
}

export function isKnownResponseCode(code: number): boolean {
  return responseCodeDescriptions.has(code);
}

export function registerResponseCode(code: number, description: string): void {
  responseCodeDescriptions.set(code, description);
}

export function knownResponseCodes(): ReadonlyArray<[number, string]> {
  return Array.from(responseCodeDescriptions.entries());
}
